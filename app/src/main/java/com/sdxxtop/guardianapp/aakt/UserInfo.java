public class UserInfo {
    public String date;
    public String update;
    public String title;
    public String status;
    public String score;
    public String type;

    public UserInfo(String date, String update, String title, String status, String score, String type) {
        this.date = date;
        this.update = update;
        this.title = title;
        this.status = status;
        this.score = score;
        this.type = type;
    }
}
